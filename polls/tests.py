# This file contains automated tests for the polls Django application.
# The tests check the behaviour of models, views, database transactions,
# and the live server using Django's testing framework.
#
# Different Django test classes are used:
# - TestCase: tests models and views using the test database
# - SimpleTestCase: tests logic that does not require the database
# - TransactionTestCase: tests database behaviour with real transactions
# - LiveServerTestCase: starts a temporary test server and checks responses


import datetime
from urllib.request import urlopen

from django.utils import timezone
from django.urls import reverse
# New imports for the unittest 
from django.test import TestCase, SimpleTestCase, TransactionTestCase, LiveServerTestCase
from .models import Question


# Utility function to create a question
def create_question(question_text, days):
    """
    Create a question with the given `question_text` and published the
    given number of `days` offset to now (negative for questions published
    in the past, positive for questions that have yet to be published).
    """
    time = timezone.now() + datetime.timedelta(days=days)
    return Question.objects.create(question_text=question_text, pub_date=time)


# Test case for the Question model
class QuestionModelTests(TestCase):
    def test_was_published_recently_with_future_question(self):
        """
        was_published_recently() returns False for questions whose pub_date
        is in the future.
        """
        time = timezone.now() + datetime.timedelta(days=30)
        future_question = Question(pub_date=time)
        self.assertIs(future_question.was_published_recently(), False)

    def test_was_published_recently_with_old_question(self):
        """
        was_published_recently() returns False for questions whose pub_date
        is older than 1 day.
        """
        time = timezone.now() - datetime.timedelta(days=1, seconds=1)
        old_question = Question(pub_date=time)
        self.assertIs(old_question.was_published_recently(), False)

    def test_was_published_recently_with_recent_question(self):
        """
        was_published_recently() returns True for questions whose pub_date
        is within the last day.
        """
        time = timezone.now() - datetime.timedelta(hours=23, minutes=59, seconds=59)
        recent_question = Question(pub_date=time)
        self.assertIs(recent_question.was_published_recently(), True)


# Test case for the index view
class QuestionIndexViewTests(TestCase):
    def test_no_questions(self):
        """
        If no questions exist, an appropriate message is displayed.
        """
        response = self.client.get(reverse("polls:index"))
        self.assertEqual(response.status_code, 200)
        self.assertContains(response, "No polls are available.")
        self.assertQuerySetEqual(response.context["latest_question_list"], [])

    def test_past_question(self):
        """
        Questions with a pub_date in the past are displayed on the
        index page.
        """
        question = create_question(question_text="Past question.", days=-30)
        response = self.client.get(reverse("polls:index"))
        self.assertQuerySetEqual(
            response.context["latest_question_list"],
            [question],
        )

    def test_future_question(self):
        """
        Questions with a pub_date in the future aren't displayed on
        the index page.
        """
        create_question(question_text="Future question.", days=30)
        response = self.client.get(reverse("polls:index"))
        self.assertContains(response, "No polls are available.")
        self.assertQuerySetEqual(response.context["latest_question_list"], [])

    def test_future_question_and_past_question(self):
        """
        Even if both past and future questions exist, only past questions
        are displayed.
        """
        question = create_question(question_text="Past question.", days=-30)
        create_question(question_text="Future question.", days=30)
        response = self.client.get(reverse("polls:index"))
        self.assertQuerySetEqual(
            response.context["latest_question_list"],
            [question],
        )

    def test_two_past_questions(self):
        """
        The questions index page may display multiple questions.
        """
        question1 = create_question(question_text="Past question 1.", days=-30)
        question2 = create_question(question_text="Past question 2.", days=-5)
        response = self.client.get(reverse("polls:index"))
        self.assertQuerySetEqual(
            response.context["latest_question_list"],
            [question2, question1],
        )


# Test case for the Detail view
class QuestionDetailViewTests(TestCase):
    def test_future_question(self):
        """
        The detail view of a question with a pub_date in the future
        returns a 404 not found.
        """
        future_question = create_question(question_text="Future question.", days=5)
        url = reverse("polls:detail", args=(future_question.id,))
        response = self.client.get(url)
        self.assertEqual(response.status_code, 404)

    def test_past_question(self):
        """
        The detail view of a question with a pub_date in the past
        displays the question's text.
        """
        past_question = create_question(question_text="Past Question.", days=-5)
        url = reverse("polls:detail", args=(past_question.id,))
        response = self.client.get(url)
        self.assertContains(response, past_question.question_text)

# New tests
# SimpleTestCase is used for tests that do not require access to the database.
# This test verifies that the URL name "polls:index" correctly resolves to the expected URL path.
class PollsSimpleTests(SimpleTestCase):
    def test_index_url_resolves(self):
        url = reverse("polls:index")
        self.assertEqual(url, "/polls/")

# TransactionTestCase is used when testing database behaviour that involves transactions.
# This test checks that a Question object can be created in the database and counted correctly.
class PollsTransactionTests(TransactionTestCase):
    def test_question_can_be_created_in_database(self):
        Question.objects.create(
            question_text="Transaction test question",
            pub_date=timezone.now()
        )
        self.assertEqual(Question.objects.count(), 1)

# LiveServerTestCase starts a temporary test server and allows HTTP requests to be sent to it.
# This test checks that the Django test server starts correctly and returns a valid HTTP response.
class PollsLiveServerTests(LiveServerTestCase):
    def test_live_server_is_running(self):
        response = urlopen(f"{self.live_server_url}/polls/")
        self.assertEqual(response.status, 200)